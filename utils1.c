#include "cub3d.h"

void	str(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
		write (1, &s[i++], 1);
}

void	exit_msg(char *msg)
{
	str(msg);
	exit (0);
}

